package dto;

public class General {
	private int g_no;
	private String g_id;
	private String g_pw;
	private String g_name;
	private String g_email;
	private int g_phone;
	private String g_age;
	
	public int getG_no() {
		return g_no;
	}
	public void setG_no(int g_no) {
		this.g_no = g_no;
	}
	public String getG_id() {
		return g_id;
	}
	public void setG_id(String g_id) {
		this.g_id = g_id;
	}
	public String getG_pw() {
		return g_pw;
	}
	public void setG_pw(String g_pw) {
		this.g_pw = g_pw;
	}
	public String getG_name() {
		return g_name;
	}
	public void setG_name(String g_name) {
		this.g_name = g_name;
	}
	public String getG_email() {
		return g_email;
	}
	public void setG_email(String g_email) {
		this.g_email = g_email;
	}
	public int getG_phone() {
		return g_phone;
	}
	public void setG_phone(int g_phone) {
		this.g_phone = g_phone;
	}
	public String getG_age() {
		return g_age;
	}
	public void setG_age(String g_age) {
		this.g_age = g_age;
	}


}
